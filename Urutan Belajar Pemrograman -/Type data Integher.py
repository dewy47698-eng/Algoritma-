# Contoh penggunaan tipe data integer di Python

angka1 = 10       # integer positif
angka2 = -25      # integer negatif
angka3 = 1000     # integer besar

print("Angka 1 :", angka1)
print("Angka 2 :", angka2)
print("Angka 3 :", angka3)

# Operasi aritmatika
jumlah = angka1 + angka3
selisih = angka3 - angka2
perkalian = angka1 * 5

print("\nHasil Operasi:")
print("Jumlah      :", jumlah)
print("Selisih     :", selisih)
print("Perkalian   :", perkalian)
