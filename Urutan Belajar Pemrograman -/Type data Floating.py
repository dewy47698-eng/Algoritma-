# Contoh penggunaan tipe data floating point di Python

angka1 = 3.14      # floating point
angka2 = -7.5      # floating point negatif
angka3 = 12.987    # floating point lebih panjang

print("Angka 1 :", angka1)
print("Angka 2 :", angka2)
print("Angka 3 :", angka3)

# Operasi aritmatika floating point
jumlah = angka1 + angka3
perkalian = angka2 * 2
pembagian = angka3 / angka1

print("\nHasil Operasi Floating Point:")
print("Jumlah     :", jumlah)
print("Perkalian  :", perkalian)
print("Pembagian  :", pembagian)
